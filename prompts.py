# prompts.py
# 各种公文类型的Prompt模板
# 这个文件的核心逻辑：告诉AI"你是公文专家，按照这个格式生成"

SYSTEM_PROMPT = """你是一位资深党政机关公文写作专家，熟悉《党政机关公文格式》国家标准（GB/T 9704-2012）。

公文格式要求：
- 标题：概括公文主要内容，一般由发文机关+事由+文种组成
- 主送机关：顶格写，冒号结尾
- 正文：开头交代背景依据，主体分条列项
- 一级标题用"一、二、三、"加黑体
- 二级标题用"（一）（二）（三）"
- 三级标题用"1.2.3."
- 落款：发文机关署名在右下角，成文日期在署名下方，用阿拉伯数字
- 抄送：如有需要写在版记部分

生成公文时，只输出公文正文内容，不要添加任何解释或说明。保持格式严谨、语言规范。"""


def get_prompt(doc_type, material, org_name, date, extra=""):
    """
    根据公文类型生成对应的用户Prompt
    
    doc_type: 公文类型
    material: 用户输入的原始素材
    org_name: 发文机关名称
    date: 成文日期
    extra: 附加要求（主送机关、抄送单位等）
    """
    
    templates = {
        "通知": f"""请根据以下素材，生成一份标准格式的【通知】。

发文机关：{org_name}
成文日期：{date}
{f'附加要求：{extra}' if extra else ''}

原始素材：
{material}

请按照以下结构生成：
标题（关于XX的通知）
主送机关：
正文（背景依据 + 分条列项的具体事项）
落款（{org_name}，{date}）""",

        "情况汇报": f"""请根据以下素材，生成一份标准格式的【情况汇报】。

发文机关：{org_name}
成文日期：{date}
{f'附加要求：{extra}' if extra else ''}

原始素材：
{material}

请按照以下结构生成：
标题（关于XX情况的汇报）
主送机关：
正文（基本情况 + 主要做法/存在问题 + 下一步打算）
落款（{org_name}，{date}）""",

        "简报": f"""请根据以下素材，生成一份标准格式的【简报】。

发文机关：{org_name}
成文日期：{date}
{f'附加要求：{extra}' if extra else ''}

原始素材：
{material}

请按照以下结构生成：
标题（简洁概括核心内容）
正文（导语 + 主体事实 + 必要背景）
落款（{org_name}，{date}）""",

        "会议纪要": f"""请根据以下素材，生成一份标准格式的【会议纪要】。

发文机关：{org_name}
成文日期：{date}
{f'附加要求：{extra}' if extra else ''}

原始素材：
{material}

请按照以下结构生成：
标题（XX会议纪要）
会议基本信息（时间、地点、出席人员、主持人）
会议内容（分议题或分发言人整理）
会议决定事项
落款（{org_name}，{date}）""",
    }
    
    return templates.get(doc_type, templates["通知"])